    seen = set()
    merged = []
    for ex in base_examples:
        es = ex.get("es", "").strip()
        if es and es not in seen:
            seen.add(es)
            merged.append(ex)
    th["examples"] = listen_fn(week_meta, new_vocab, taught_tokens, merged, limit=6)
