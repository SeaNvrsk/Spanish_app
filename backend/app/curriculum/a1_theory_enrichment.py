def enrich_a1_theory(th: dict, week_num: int, day_in_week: int, new_vocab: list, taught_tokens: set, week_meta: dict, listen_fn) -> None:
    """Apply A1 enrichment to a theory dict in place."""
    pack = A1_THEORY.get(week_num)
    if not pack:
        return

    if pack.get("grammar_sections"):
        th["grammar_sections"] = pack["grammar_sections"]
        # Flat list fallback for older clients
        th["grammar_points"] = {
            lang: [f"{s['title']}: {s['text']}" if s.get("title") else s["text"] for s in sections]
            for lang, sections in pack["grammar_sections"].items()
        }

    day_focus = pack.get("day_focus", {}).get(day_in_week)
    if day_focus:
        word_list = ", ".join(v["es"] for v in new_vocab[:5])
        if len(new_vocab) > 5:
            word_list += "…"
        th["day_focus"] = t(
            f"{day_focus['en']} Today's words: {word_list}.",
            f"{day_focus['ru']} Слова сегодня: {word_list}.",
            f"{day_focus['es']} Palabras de hoy: {word_list}.",
        )

